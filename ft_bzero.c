#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*str_c;

	str_c = (char *)s;
	i = 0;
	while (i < n)
	{
		*(str_c + i) = 0;
		i++;
	}
}
